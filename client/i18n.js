export const LANG_NAMES = { ru: 'Русский', en: 'English', pl: 'Polski' };

let _lang = localStorage.getItem('da_lang') || 'ru';

export function getLang() { return _lang; }

export function setLang(l) {
  if (!TRANSLATIONS[l]) return;
  _lang = l;
  localStorage.setItem('da_lang', l);
}

/**
 * Translate a key, optionally interpolating {placeholders}.
 * e.g. t('typing_one', { name: 'Alice' }) → 'Alice is typing...'
 */
export function t(key, vars = {}) {
  const dict = TRANSLATIONS[_lang] || TRANSLATIONS.ru;
  let str = dict[key] ?? TRANSLATIONS.ru[key] ?? key;
  for (const [k, v] of Object.entries(vars)) {
    str = str.replace(new RegExp(`\\{${k}\\}`, 'g'), v);
  }
  return str;
}
